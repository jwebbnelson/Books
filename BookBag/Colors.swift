import Foundation
import UIKit

extension UIColor {
    
    static func separatorGrey() -> UIColor {
       return UIColor(red:0, green:0, blue:0, alpha:0.12)
    }
    
    static func actionGreen() -> UIColor {
        return UIColor(red:0.00, green:0.90, blue:0.46, alpha:1.00)
    }
    
    static func whiteSmoke() -> UIColor {
        return UIColor(red:0.98, green:0.98, blue:0.98, alpha:1.00)
    }
    
    
    
}